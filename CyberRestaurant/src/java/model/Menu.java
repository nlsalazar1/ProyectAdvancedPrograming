
package model;

public class Menu {
    String idMenu;
    String nameMenu;

    public Menu(String idMenu, String nameMenu) {
        this.idMenu = idMenu;
        this.nameMenu = nameMenu;
    }

    public String getIdMenu() {
        return idMenu;
    }

    public void setIdMenu(String idMenu) {
        this.idMenu = idMenu;
    }

    public String getNameMenu() {
        return nameMenu;
    }

    public void setNameMenu(String nameMenu) {
        this.nameMenu = nameMenu;
    }
    
    
    
}
